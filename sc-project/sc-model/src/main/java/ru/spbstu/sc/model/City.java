package ru.spbstu.sc.model;

public class City extends BaseEntity {

	Country country;
	String name;
	
	public Country getCountry() {
		return country;
	}
	public void setCountry(Country country) {
		this.country = country;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
